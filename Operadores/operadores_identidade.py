saldo = 1000

limite = 500 

print(saldo == 1000)  # True, saldo é igual a 1000
print(saldo != 500)   # True, saldo não é igual a 500   
print(saldo < 1500)   # True, saldo é menor que 1500
print(saldo > 500)    # True, saldo é maior que 500 
print(saldo <= 1000)  # True, saldo é menor ou igual a 1000
print(limite == 500)  # True, limite é igual a 500